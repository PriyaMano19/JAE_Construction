     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, int $id)
    {
        $request->validate([
            'cat_name'=>'required',
            'cat_description'=>'required',
            
        ]);
        $category = Category::where('id',$id)->get();

        Category::where('id', $id)->update([
